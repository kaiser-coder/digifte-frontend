<template lang="" >
  <div class="subcontent">
    <div class="row justify-center">
      <div style="display: flex; max-width: 250%; width: 100%; height: 400px;">
        <q-calendar-day
          ref="calendar"
          v-model="selectedDate"
          view="week"
          animated
          bordered
          no-active-date
        >
          <template #head-day-event="{ scope: { timestamp } }">
            <div style="display: flex; justify-content: center; flex-wrap: wrap; padding: 2px;">
              <template
                v-for="event in eventsMap[timestamp.date]"
                :key="event.id"
              >
                <q-badge
                  v-if="!event.time"
                  :class="badgeClasses(event, 'header')"
                  :style="badgeStyles(event, 'header')"
                  style="width: 100%; cursor: pointer; height: 12px; font-size: 10px; margin: 1px;"
                >
                  <span class="title q-calendar__ellipsis">
                    {{ event.title }}
                    <q-tooltip>{{ event.details }}</q-tooltip>
                  </span>
                </q-badge>

                <q-badge
                  v-else
                  :class="badgeClasses(event, 'header')"
                  :style="badgeStyles(event, 'header')"
                  style="margin: 1px; width: 10px; max-width: 10px; height: 10px; max-height: 10px"
                  @click="scrollToEvent(event)"
                >
                  <q-tooltip>
                    {{ event.time + ' - ' + event.details }}
                </q-tooltip>
                </q-badge>
              </template>
            </div>
          </template>

          <template #day-body="{ scope: { timestamp, timeStartPos, timeDurationHeight } }">
            <template
              v-for="event in getEvents(timestamp.date)"
              :key="event.id"
            >
              <div
                v-if="event.time !== undefined"
                class="my-event"
                :class="badgeClasses(event, 'body')"
                :style="badgeStyles(event, 'body', timeStartPos, timeDurationHeight)"
              >
                <span class="title q-calendar__ellipsis">
                  {{ event.title }}
                  <q-tooltip>
                    <ul style="list-style: none;">
                      <li>Durée: {{ event.details[0] }}</li>
                      <li>Lien du Meeting: {{ event.details[1] }}</li>
                      <li>Date de début: {{ event.details[2] }}</li>
                    </ul>
                  </q-tooltip>
                </span>
              </div>
            </template>
          </template>
        </q-calendar-day>
      </div>
    </div>
  </div>
</template>

<script setup>
/*eslint-disable*/
import {
  QCalendarDay,
  addToDate,
  parseTimestamp,
  isBetweenDates,
  today,
  parsed,
  parseDate,
  parseTime
} from '@quasar/quasar-ui-qcalendar/src/QCalendarDay.js'
import '@quasar/quasar-ui-qcalendar/src/QCalendarVariables.sass'
import '@quasar/quasar-ui-qcalendar/src/QCalendarTransitions.sass'
import '@quasar/quasar-ui-qcalendar/src/QCalendarDay.sass'

import { defineComponent, computed, defineProps, onMounted } from 'vue';
//import NavigationBar from '../components/NavigationBar.vue'



// The function below is used to set up our demo data
const CURRENT_DAY = new Date()
function getCurrentDay (day) {
  const newDay = new Date(CURRENT_DAY)
  newDay.setDate(day)
  const tm = parseDate(newDay)
  return tm.date
}

const selectedDate = today();
const props = defineProps({
  lessons: Array
});

const handleTime = (dateD) => {
  let hrs = dateD.getHours()
  let mins = dateD.getMinutes()
  if(hrs<=9)
      hrs = '0' + hrs
  if(mins<10)
    mins = '0' + mins
  const postTime= hrs + ':' + mins
  return postTime
}

const events = computed(() => {
    // Format data
    let custom = [];
    props.lessons.forEach((d) => {
      const date = new Date(d.start_date);
      // console.log('Time => ', handleTime(date))
      custom.push({
        id: d._id,
        title: d.name,
        details: [d.duration, d.zoom_url, date],
        date: getCurrentDay(date.getDate()), // start_date
        time: handleTime(date),
        duration: d.duration,
        bgcolor: d.bgcolor
      })
    })
    return custom
});

onMounted(() => {
  console.log('Lessons => ', props.lessons);
})

// Dispatch events by date
const eventsMap = computed (() => {
    const map = {}
    events.value.forEach(event => {
    if (!map[ event.date ]) {
        map[ event.date ] = []
    }
    map[ event.date ].push(event)
    if (event.days) {
        let timestamp = parseTimestamp(event.date)
        let days = event.days
        do {
          timestamp = addToDate(timestamp, { day: 1 })
          if (!map[ timestamp.date ]) {
              map[ timestamp.date ] = []
          }
          map[ timestamp.date ].push(event)
        } while (--days > 0)
    }
    })
    console.log('Mapped => ', map)
    return map
})

function badgeClasses (event, type) {
    const isHeader = type === 'header'
    return {
    [ `text-white bg-${ event.bgcolor }` ]: true,
    'full-width': !isHeader && (!event.side || event.side === 'full'),
    'left-side': !isHeader && event.side === 'left',
    'right-side': !isHeader && event.side === 'right',
    'rounded-border': true
    }
}

function badgeStyles (event, type, timeStartPos = undefined, timeDurationHeight = undefined) {
    const s = {}
    if (timeStartPos && timeDurationHeight) {
    s.top = timeStartPos(event.time) + 'px'
    s.height = timeDurationHeight(event.duration) + 'px'
    }
    s[ 'align-items' ] = 'flex-start'
    return s
}

function getEvents (dt) {
    // get all events for the specified date

    const events = eventsMap.value[dt] || []
    // console.log('Date event => ', dt);

    if (events.length === 1) {
      events[ 0 ].side = 'full'
    }
    else if (events.length === 2) {
      // this example does no more than 2 events per day
      // check if the two events overlap and if so, select
      // left or right side alignment to prevent overlap
      const startTime = addToDate(parsed(events[ 0 ].date), { minute: parseTime(events[ 0 ].time) })
      const endTime = addToDate(startTime, { minute: events[ 0 ].duration })
      const startTime2 = addToDate(parsed(events[ 1 ].date), { minute: parseTime(events[ 1 ].time) })
      const endTime2 = addToDate(startTime2, { minute: events[ 1 ].duration })
      if (isBetweenDates(startTime2, startTime, endTime, true) || isBetweenDates(endTime2, startTime, endTime, true)) {
          events[ 0 ].side = 'left'
          events[ 1 ].side = 'right'
      }
      else {
          events[ 0 ].side = 'full'
          events[ 1 ].side = 'full'
      }
    }

    return events
}

</script>

